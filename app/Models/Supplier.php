<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Model;

class Supplier extends Model
{
    protected $fillable = [
        'supplier_name',
        'contact_number',
        'email',
        'address',
        'delivery_status'
    ];

    public function inventoryItems()
    {
        return $this->hasMany(InventoryItem::class);
    }
}
